package algo_and_datastruc;

public class Findingcomplexity {

	public static void main(String[] args) {
		
	int n=100;
	for(int i=1;i<n;) {
		System.out.println("the value of i is "+i);
		i=i*2;
		
	}
	
	
	System.out.println("\n\n\n");
	
	
	for(int i=n;i>0;) {
		System.out.println("the value of i is "+i);
		i=i/2;
	}

	}

}
